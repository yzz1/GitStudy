#include "callinfo.h"

CallInfo::CallInfo()
{
}

void CallInfo::setRequestType(QString value)
{
    requestType = value;
}

QString CallInfo::getRequestType()
{
    return requestType;
}

void CallInfo::setCallName(QString value)
{
    callName = value;
}

QString CallInfo::getCallName()
{
    return callName;
}

void CallInfo::setCallCode(QString value)
{
    callCode = value;
}

QString  CallInfo::getCallCode()
{
    return callCode;
}

void CallInfo::setCallChannel(QString value)
{
    callChannel = value;
}

QString CallInfo::getCallChannel()
{
    return callChannel;
}

void CallInfo::setTx(QString value)
{
    tx = value;
}

QString CallInfo::getTx()
{
    return tx;
}

void CallInfo::setRx(QString value)
{
    rx = value;
}

QString CallInfo::getRx()
{
    return rx;
}

void CallInfo::setIsCall(bool value)
{
    isCall = value;
}

bool CallInfo::getIsCall()
{
    return isCall;
}

QString CallInfo::toString()
{
    return QString("requestType=").append(requestType).append(QString("callName=")).append(callName).append(
               QString("callCode=")).append(callCode).append(QString("callChannel=")).append(callChannel).append(
               QString("tx=")).append(tx).append(QString("rx=")).append(rx).append(QString("callType=")).append(callType).append(
               QString("isCall=")).append(isCall);
}
